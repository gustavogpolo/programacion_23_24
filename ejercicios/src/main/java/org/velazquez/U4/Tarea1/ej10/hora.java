package org.velazquez.U4.Tarea1.ej10;

public class hora {

    private int hora;

    private int minuto;

    private int segundo;

    private int incremento_segundos;

    public int getHora() {
        return hora;
    }

    public void setHora(int hora) {
        this.hora = hora;
    }

    public int getMinuto() {
        return minuto;
    }

    public void setMinuto(int minuto) {
        this.minuto = minuto;
    }

    public int getSegundo() {
        return segundo;
    }

    public void setSegundo(int segundo) {
        this.segundo = segundo;
    }

    public int getIncremento_segundos() {
        return incremento_segundos;
    }

    public void setIncremento_segundos(int incremento_segundos) {
        this.incremento_segundos = incremento_segundos;
    }
}
